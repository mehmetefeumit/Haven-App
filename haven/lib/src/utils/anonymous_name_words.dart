/// Curated word lists for the anonymous display-name generator.
///
/// The generator (`anonymous_name_generator.dart`) forms a default display
/// name as "Adjective Archetype" (e.g. "Quiet Wanderer") by picking one word
/// from each list uniformly at random. The cross product is
/// 482 x 376 = 181,232 unique names — ample entropy for a pre-filled,
/// user-editable default (uniqueness is not required, so no numeric suffix is
/// appended, matching the White Noise reference app).
///
/// These lists are maintained here deliberately, with NO third-party
/// dependency (White Noise uses the `unique_names_generator` package; Haven
/// does not — do not "fix" this back toward that package).
///
/// ## Curation rules (read before editing)
///
/// - ASCII lowercase, single word, 2-12 characters. No spaces, hyphens,
///   apostrophes, digits, or accents. Store lowercase; the generator
///   capitalizes centrally.
/// - No duplicates within a list, and no word may appear in BOTH lists
///   (enforced by `anonymous_name_generator_test.dart`).
/// - FAMILY-FRIENDLY IN ISOLATION: every adjective is randomly paired with
///   ANY archetype and vice versa (~181,232 pairings — nobody reviews them
///   all), so each word must read inoffensively next to ANY word from the
///   other list: no slur, sexual, violent, drug, or insulting reading, and no
///   double-entendre.
/// - Archetypes are neutral-to-heroic single-word role/persona nouns; avoid
///   loaded roles (no "slave", "savage", "brute", etc.).
/// - No proper nouns, brand names, franchise/fictional-character names, or
///   place names. Neutral-to-positive tone throughout.
